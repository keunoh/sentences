그녀는 자신이 알게 된 모든 것을 거부하지는 않았다.
하지만 그녀는 자기가 바라는 대로 될 수 있다고 생각하며 자신을 속여 왔다는 것을 깨달았다.
그녀는 마치 잠에서 깨어난 것 같았다.
그녀는 위선과 오만 없이 자기가 도달하고자 하는 그 경지를 고집한다는 것이 얼마나 어려운가를 절실히 느꼈다...

그 순간 그는 이상하게도 자신이 그 꿈을 버리지 않았고 앞으로도 그 꿈 없이 살 수 없다는 느낌을 받았다.

알렉세이 알렉산드로비치는 인생과 대면한 것이다. 
그의 아내가 그가 아닌 다른 누군가를 사랑할 수도 있다는 사실과 맞닥뜨린 것이다.
